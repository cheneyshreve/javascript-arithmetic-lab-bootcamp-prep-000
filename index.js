function add(a,b) {
  return a + b
}

function subtract(a,b) {
  return a - b
}

function multiply(a,b) {
  return a * b
}

function divide(a,b) {
  return a / b
}

function inc(n) {
  var a
  return a+1+n
}

function dec(n) {
  var a
  return a-1-n
}

function makeInt(n) {
  return parseInt(n,10)
}

function preserveDecimal(n) {
   return parseFloat(n)
}
